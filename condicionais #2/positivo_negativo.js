/*
Solicite um número ao usuário e use if/else para verificar se o número é positivo, negativo ou zero.
*/

let numero = 10;

if(numero > 0) {
    console.log(`O número ${numero} é positivo!`);
} else if(numero < 0) {
    console.log(`O número ${numero} é negativo!`);
} else {
    console.log(`O número ${numero} é neutro!`);
}
