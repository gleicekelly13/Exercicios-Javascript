/*
Operadores de Comparação
Crie um programa que:

Compare dois números (x e y) e exiba no console:
Se x é maior que y.
Se x é igual a y (use tanto == quanto === para observar a diferença).
Se x é diferente de y.
*/

let x = 10;
let y = "10";

console.log("x é maior que y? ", x < y);
console.log("x é maior ou igual a y?", x <= y);
console.log("x é igual a y? ", x == y, x === y);
console.log("x é diferente de y? ", x != y, x !== y);
